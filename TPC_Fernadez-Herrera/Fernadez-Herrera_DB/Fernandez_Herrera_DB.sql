)
GO 
create table RAMS(
 IDCOMPONENTE BIGINT NOT NULL FOREIGN KEY REFERENCES COMPONENTES(ID),
 DDR INT NOT NULL,
 CAPACIDAD INT NOT NULL,
 VELOCIDAD INT NOT NULL,
)
GO
create table DISCORIGIDOS(
 IDCOMPONENTE BIGINT NOT NULL FOREIGN KEY REFERENCES COMPONENTES(ID),
