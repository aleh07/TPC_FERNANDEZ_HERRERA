POTENCIA VARCHAR NOT NULL,
CERTIFICACION VARCHAR
)
GO
create table GABIENTES(
IDCOMPONENTE BIGINT NOT NULL FOREIGN KEY REFERENCES COMPONENTES(ID),
TAMANIOMOTHER VARCHAR,
CANTUSB INT
)
GO
create table PLACASDEVIDEO(
IDCOMPONENTE BIGINT NOT NULL FOREIGN KEY REFERENCES COMPONENTES(ID),
